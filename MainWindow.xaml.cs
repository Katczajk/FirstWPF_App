        public MainWindow()
        {
            InitializeComponent();
        }

        private void button1_Click(object sender, RoutedEventArgs e)
        {
            double ActHeight;
            if (button1.IsMouseOver)
            {
                ActHeight = button1.ActualHeight;
                textbox1.Text = ActHeight.ToString();
            }
            else {
                textbox1.Text = "";
            }
        }
    }
